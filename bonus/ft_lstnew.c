#include "../include/libft.h"

t_list	*ft_lstnew(void *content)
{
	t_list	*first;

	first = (t_list *)malloc(sizeof(t_list));
	if (first)
	{
		first->content = content;
		first->next = NULL;
		return (first);
	}
	return (0);
}
